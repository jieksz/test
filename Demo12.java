public class Demo12 {
    public static void main(String[] args) {
        // 字符串参与运算
        System.out.println("5+5" + 5 + 5);
        System.out.println(5 + 5 + "=10");
        System.out.println("==========================");
        // 字符参与运算
        char c = 'a';
        System.out.println(c + 1);
        System.out.println("c="+c);
    }
}
